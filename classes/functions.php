/**
 * Copy a file, or recursively copy a folder and its contents
 * @author      Aidan Lister <aidan@php.net>
 * @version     1.0.1
 * @link        http://aidanlister.com/2004/04/recursively-copying-directories-in-php/
 * @param       string   $source    Source path
 * @param       string   $dest      Destination path
 * @param       int      $permissions New folder creation permissions
 * @param       bool    $overwrite Overwrite file or add incremental number
 * @return      bool     Returns true on success, false on failure
 */
function xcopy($source, $dest, $permissions = 0755,$overwrite = true)
{
    // Check for symlinks
    if (is_link($source)) {
        return symlink(readlink($source), $dest);
    }

    // Simple copy for a file
    if (is_file($source)) {
        if($overwrite)
        {
            return copy($source, $dest);
        }else{
            if(file_exists($dest))
            {
                $dest  = file_newname($dest);

                return copy($source, $dest);
            }else{
                return copy($source, $dest);                
            }
        }
    }

    // Make destination directory
    if (!is_dir($dest)) {
        mkdir($dest, $permissions);
    }

    // Loop through the folder
    $dir = dir($source);
    while (false !== $entry = $dir->read()) {
        // Skip pointers
        if ($entry == '.' || $entry == '..') {
            continue;
        }

        // Deep copy directories
        xcopy("$source/$entry", "$dest/$entry", $permissions,$overwrite);
    }

    // Clean up
    $dir->close();
    return true;
}

function file_newname($source){
    $path_parts = pathinfo($source);
    $filename = $path_parts['basename'];
    $path = $path_parts['dirname'];
    
    if ($pos = strrpos($filename, '.')) {
           $name = substr($filename, 0, $pos);
           $ext = substr($filename, $pos);
    } else {
           $name = $filename;
    }

    $newpath = $path.'/'.$filename;
    $newname = $filename;
    $counter = 1;
    while (file_exists($newpath)) {
           $newname = $name .'_'. $counter . $ext;
           $newpath = $path.'/'.$newname;
           $counter++;
     }
    return $newpath;
}

