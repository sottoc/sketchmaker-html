function get_sort($field = '')
{
    $tmpf = $field;
    $operand = '';
    $get_field = false;
    if(isset($_GET['sort']))
    {
        $operand = substr($_GET['sort'],0,1);
        $get_field = str_replace('-','',$_GET['sort']);
        
        $field = $field ? $field : $get_field;
    }
    
    $url = ($operand  == '-' ? '': '-').$field;
    $asc_desc = $operand=='-' ? 'DESC' : 'ASC';
    return [
        'url'=>$url,
        'order'=> $field  ? $field.' '.$asc_desc : null,
        'asc_desc_class' => $get_field == $tmpf  ? 'sort_'.strtolower($asc_desc) : ''
    ];
}


